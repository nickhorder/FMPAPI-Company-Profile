    public class TickerEnumerator
    {
        int nIndex;
        TickerStore collection;
        public TickerEnumerator(TickerStore coll)
        {
            collection = coll;
            nIndex = -1;
        }
        public bool MoveNext()
        {
            nIndex++;
            return (nIndex < collection.items.Length);
        }

        public string Current => collection.items[nIndex];
